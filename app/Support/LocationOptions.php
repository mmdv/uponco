    /**
     * Get the list of selectable countries, sorted by name.
     *
     * @return list<array{value: string, label: string}>
     */
    public static function countries(): array
    {
        $countries = array_map(fn (string $code): array => [
            'value' => $code,
            'label' => self::countryName($code),
        ], self::COUNTRY_CODES);

        usort($countries, fn (array $a, array $b): int => strcmp($a['label'], $b['label']));

        return $countries;
    }

    /**
     * Get the English display name for an ISO 3166-1 alpha-2 country code.
     */
    public static function countryName(string $code): string
    {
        return Locale::getDisplayRegion('-'.$code, 'en') ?: $code;
    }

    /**
     * Get the list of valid country codes.
     *
     * @return list<string>
     */
    public static function countryCodes(): array
    {
        return self::COUNTRY_CODES;
    }

    /**
     * Get the list of selectable timezones.
     *
     * @return list<array{value: string, label: string}>
     */
    public static function timezones(): array
    {
        return array_map(fn (string $timezone): array => [
            'value' => $timezone,
            'label' => str_replace('_', ' ', $timezone),
        ], timezone_identifiers_list());
    }
